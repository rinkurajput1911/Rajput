package Polymorphism;

public class Son extends Father {
	//sub class/child class
	public void car()
	{
		System.out.println("Car: Fortuner" ); //override
	}
	public void money()
	{
		System.out.println("Money: 1lakh"); //override
	}
	public void home()
	{
		System.out.println("Home: 1BHK"); //override
	}

}
